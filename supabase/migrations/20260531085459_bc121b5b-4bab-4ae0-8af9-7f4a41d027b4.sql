## Safety, insurance, and trust

Every booking includes $2M liability insurance that covers both the host's property and the guest's group for the duration of the rental. The insurance is built into the platform fee — there's nothing extra for the host or guest to buy.

Hosts are responsible for keeping the pool water safe and the deck free of obvious hazards. Guests are responsible for following the house rules, not exceeding the listed guest count, and supervising any children in their group. The platform is not a substitute for adult supervision around water.

## Cancellations, refunds, and changes

Cancellation policies vary by listing — some hosts offer full refunds for cancellations made well in advance, others are stricter, especially for weekend and holiday bookings. Always read the policy before booking. If the host cancels on you, you get a full refund automatically. Full payment, refund, and dispute details live in the [FAQ](/p/faq).

