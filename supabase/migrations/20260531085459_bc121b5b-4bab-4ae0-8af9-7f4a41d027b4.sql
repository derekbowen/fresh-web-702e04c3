### Pricing — what to expect as a guest

Hourly rates typically range from $40–$150/hour depending on the city, pool size, amenities, and time of year. A typical 3-hour party booking lands between $150 and $400 plus any heater or cleaning fees the host lists. Larger groups, weekend prime time, and pools with hot tubs sit at the higher end.

## <a id="for-hosts"></a>For hosts: listing your pool

If you've got a private pool, you can earn $3,000–$10,000 a month from it without changing how you use it day to day. Here's the full path.

**Step 1: Create your listing.** Add photos, describe your pool and outdoor space, set your amenities, and write clear house rules. The more detail you publish, the more confident guests are when booking. Start at [list your pool](/p/list-your-pool) — the form takes about 15 minutes if you already have photos.

**Step 2: Set your price and availability.** You control your hourly rate, your available hours, your booking window, and your minimum booking length. You can block any date at any time. New hosts usually start a little below their target rate to earn the first few reviews, then raise prices.

**Step 3: Review and accept bookings.** For non-instant listings you review each guest request before accepting. You can message guests to ask about group size, what they're celebrating, or whether they need the heater.

**Step 4: Host your guests.** Make sure the pool is clean, the amenities are ready, and the rules are clearly communicated. Most hosts greet guests briefly at arrival, then give them privacy.

**Step 5: Get paid.** Payment is released to you after the rental completes. Pool Rental Near Me handles all payment processing — no cash, no invoicing, no chasing payments.

**Step 6: Build your reputation.** Every completed booking earns you a review. Hosts with strong reviews get more bookings and can raise their hourly rate over time.

### What hosts actually earn

Most active hosts run 8–20 bookings a month in season. At a $60–$120/hour rate with 3-hour average bookings, that lands in the $1,500–$7,000/month range. Hosts with premium pools (heater, hot tub, party-ready amenities, photogenic backyard) in high-demand cities clear $10,000+ in peak months. The flat 10% host fee means you keep $90 of every $100 booked, which is meaningfully better than competing platforms.

