## <a id="for-guests"></a>For guests: booking a private pool

**Step 1: Search.** Go to [search pools near you](/s), enter your city or zip code, and filter by date, group size, amenities, and pool features. Results show verified listings with photos, hourly pricing, and host reviews. If you want to scope a specific city first, browse one of our [city pages](/p/faq) and click through to local listings.

**Step 2: Pick your listing.** Read the listing description carefully. It covers the pool's features, the house rules, what's included (towels, restroom access, grill, speakers), and what's not. Check the host's reviews from past guests for an unfiltered view. If something is unclear — heater fee, dog policy, parking — message the host before you book.

**Step 3: Request or book.** Depending on the listing you can instant-book or send a booking request. Hosts typically respond within a few hours. Bookings are by the hour, with a minimum that the host sets (often two hours).

**Step 4: Confirm and pay.** Payment is processed securely through the platform. You'll receive the full address only after your booking is confirmed — never before. If a host ever asks you to pay outside the platform, refuse and report it.

**Step 5: Show up and enjoy.** Arrive at the agreed time. The host will have the pool ready: clean water, accessible amenities, any extras they listed. Follow the house rules, treat the space respectfully, and leave it as you found it.

**Step 6: Review.** After your rental, you'll get a prompt to review the host. Honest reviews keep the community standard high and help the next guest pick a great pool.

