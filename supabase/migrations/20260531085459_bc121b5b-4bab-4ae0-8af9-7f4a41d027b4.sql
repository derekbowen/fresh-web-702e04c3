## How the marketplace works in 60 seconds

Hosts list a private pool with photos, an hourly rate, house rules, and availability. Guests search by city, date, and group size, then book by the hour. Every confirmed booking is covered by $2M liability insurance, payment runs through the platform, and both sides leave a review afterwards. The host fee is a flat 10% — lower than Swimply's 15%+ — which means hosts keep more of every booking.

