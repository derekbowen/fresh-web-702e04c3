#!/usr/bin/env bun
/**
 * Backfill content_pages by Firecrawling every /p/{slug} URL on the live
 * Sharetribe site and writing the cleaned markdown into Supabase. Same pattern
 * as backfill-heroes.ts.
 *
 * USAGE
 *   bun run scripts/backfill-content.ts                    # all URLs
 *   bun run scripts/backfill-content.ts --dry              # don't write to DB
 *   bun run scripts/backfill-content.ts --limit=50         # first 50 URLs
 *   bun run scripts/backfill-content.ts --slugs=foo,bar    # specific slugs
 *   bun run scripts/backfill-content.ts --priority=tier1   # tier1 (top 16) only
 *   bun run scripts/backfill-content.ts --force            # re-fetch even if row exists
 *
 * ENV
 *   FIRECRAWL_API_KEY
 *   SUPABASE_URL
 *   SUPABASE_SERVICE_ROLE_KEY
 *
 * INPUT
 *   url_mapping.csv at the repo root (generated by migration-plan/gen_url_mapping.py)
 *   Only rows with action=BUILD_NEW and template_type starting with anything except pp_, listing, host_profile, amenity, amenity_hub
 */
import Firecrawl from "@mendable/firecrawl-js";
import { createClient } from "@supabase/supabase-js";
import { readFileSync, writeFileSync } from "node:fs";

const FIRECRAWL_API_KEY = process.env.FIRECRAWL_API_KEY!;
const SUPABASE_URL = process.env.SUPABASE_URL!;
const SUPABASE_SERVICE_ROLE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY!;
if (!FIRECRAWL_API_KEY || !SUPABASE_URL || !SUPABASE_SERVICE_ROLE_KEY) {
  console.error("Missing env: FIRECRAWL_API_KEY, SUPABASE_URL, SUPABASE_SERVICE_ROLE_KEY");
  process.exit(1);
}

const client = new Firecrawl({ apiKey: FIRECRAWL_API_KEY });
const sb = createClient(SUPABASE_URL, SUPABASE_SERVICE_ROLE_KEY);

